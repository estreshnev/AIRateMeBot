# src/claude_psych_profile.py
import os
import httpx
import json
from src.core.ai_interface import AIProvider

ANTHROPIC_API_KEY = os.getenv("ANTHROPIC_API_KEY")
CLAUDE_API_URL = "https://api.anthropic.com/v1/messages"

HEADERS = {
    "x-api-key": ANTHROPIC_API_KEY,
    "anthropic-version": "2023-06-01",
    "content-type": "application/json"
}

class ClaudePsychProvider(AIProvider):
    async def analyze(self, profile_data: dict, analysis_type: str) -> str:
        if analysis_type != "psych":
            raise NotImplementedError("Этот провайдер поддерживает только психоанализ")
        prompt = (
            f"""
Ты — AI-профайлер. На основе информации из публичного профиля VK создай тёплый, точный и виральный психологический портрет человека. Цель — чтобы человек узнал себя, улыбнулся и захотел отправить результат другу.

Формат: markdown с эмодзи. Стиль — как умный гороскоп: доступно, приятно, с долей магии, но без фантазии. Говори не как робот, а как эмпатичный AI.

---

🔗 Профиль: https://vk.com/{profile_data['username']}

🎭 Ты — [яркое имя типажа]  
(краткая характеристика, например: «Тихий наблюдатель» или «Заряженный мечтатель»)

🧠 AI считает, что ты:
• [чертa поведения или мышления]  
• [эмоциональная или коммуникативная особенность]  
• [привычка в сети]  
• [внутренняя установка или мотивация]

🎯 Сильная сторона: [главная "суперсила"]  
⚠️ Уязвимость: [слабое место, сомнение или паттерн]

🔮 AI-прогноз:
• XX% шанс, что ты [узнаваемое поведение]  
• XX% вероятность, что ты [мемно, но реалистично]  
• XX% — ты точно [что-то, что делает отчёт личным]

📊 Уникальность: XX%  
🧬 Энергия в сети: XX/100

💬 Цитата:
> "[жизненный девиз или эмоциональный штрих]"

👥 В компании ты — [роль среди других: "тот, кто умеет слушать", "центр притяжения", и т.п.]

---

🪐 Натальная карта (AI-интерпретация):

AI составил твою цифровую натальную карту на основе стиля твоего профиля.  
(Не по дате рождения, а по тому, как ты проявляешь себя в сети)

• Знак влияния: [например, "Луна в Водолее" — интерпретируй сам]  
• Планета силы: [Меркурий, Сатурн и т.д. — что отражает стиль общения]  
• Цикл 2024: [шутливое предсказание про год]  
• Эхо прошлых жизней: [примерно — кто ты был и чем стал в цифровом мире]

✨ Предсказание:
> "[одна фраза, как личный прогноз на ближайшее]"

---

🗣️ Хочешь узнать, кто твой друг в соцсетях? Просто пришли его ссылку сюда.

---

Вот данные профиля VK:
Имя: {profile_data['name']}
Город: {profile_data['city']}
Возраст: {profile_data['age']}
Друзей: {profile_data['friends_count']}
Посты:
{chr(10).join(f'- {post}' for post in profile_data['posts'])}
"""
        )
        data = {
            "model": "claude-sonnet-4-20250514",
            "max_tokens": 700,
            "temperature": 0.7,
            "messages": [
                {"role": "user", "content": prompt}
            ]
        }
        async with httpx.AsyncClient(timeout=30) as client:
            response = await client.post(CLAUDE_API_URL, headers=HEADERS, json=data)
            response.raise_for_status()
            return response.json()["content"][0]["text"].strip()

    async def analyze_influencer(self, profile_data: dict) -> str:
        raise NotImplementedError("Анализ блогеров реализован в другом провайдере")

# Для обратной совместимости:
async def generate_report(profile_data: dict) -> str:
    return await ClaudePsychProvider().analyze(profile_data, "psych")